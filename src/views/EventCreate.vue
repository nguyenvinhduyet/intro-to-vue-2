<template>
  <div>
    <h1>Create an Event</h1>
    <form @submit.prevent="createEvent">
      <BaseSelect label="Select a category" :options="categories" 
                  v-model="event.category" @blur="$v.event.category.$touch()"
                  :class= "{ error: $v.event.category.$error }"/>
      <template v-if="$v.event.category.$error">
        <p v-if="!$v.event.category.required" 
        class="errorMessage">Category is required</p>
      </template>
        
      <h3>Name & describe your event</h3>
      <!-- <div class="field">
        <label>Title</label>
        <input v-model="event.title" type="text" placeholder="Add an event title"/>
      </div> -->
      <BaseInput 
        label="Title" 
        v-model="event.title"
        type="text"
        placeholder="Add an event title"
        class="field"
        :class="{ error: $v.event.title.$error }"
        @blur="$v.event.title.$touch()"/>
      <template v-if="$v.event.title.$error">
        <p v-if="!$v.event.title.required" 
        class="errorMessage">Title is required</p>
      </template>
        

      <!-- <div class="field">
        <label>Description</label>
        <input v-model="event.description" type="text" placeholder="Add a description"/>
      </div> -->
      <BaseInput 
        label="Description" 
        v-model="event.description"
        type="text"
        placeholder="Add a description"
        class="field"
        :class="{ error: $v.event.description.$error }"
        @blur="$v.event.description.$touch()"/>
      <template v-if="$v.event.description.$error">
        <p v-if="!$v.event.description.required" 
        class="errorMessage">Description is required</p>
      </template>

      <h3>Where is your event?</h3>
      <!-- <div class="field">
        <label>Location</label>
        <input v-model="event.location" type="text" placeholder="Add a location"/>
      </div> -->
      <BaseInput 
        label="Location" 
        v-model="event.location"
        type="text"
        placeholder="Add a location"
        class="field"
        :class="{ error: $v.event.location.$error }"
        @blur="$v.event.location.$touch()"/>
      <template v-if="$v.event.location.$error">
        <p v-if="!$v.event.location.required" 
        class="errorMessage">Location is required</p>
      </template>
      <h3>When is your event?</h3>

      <div class="field">
        <label>Date</label>
        <datepicker v-model="event.date" placeholder="Select a date"
          @opened="$v.event.date.$touch()"
          :input-class="{ error: $v.event.date.$error}"/>
        <template v-if="$v.event.date.$error">
          <p v-if="!$v.event.date.required" 
          class="errorMessage">Date is required</p>
        </template>
      </div>

      <BaseSelect label="Select a time" :options="times" v-model="event.time" class="field"
                  @blur="$v.event.time.$touch()" :class=" { error: $v.event.time.$error}"/>
      <template v-if="$v.event.time.$error">
        <p v-if="!$v.event.time.required" 
        class="errorMessage">Time is required</p>
      </template>

      <!-- <input type="submit" class="button -fill-gradient" value="Submit"/> -->
      <!-- <BaseButton @click="sendMessage">Message</BaseButton> -->
      <BaseButton type="submit" buttonClass="-fill-gradient"
                  :disabled="$v.$anyError">Submit</BaseButton>
      <p v-if="$v.$anyError" class="errorMessage">Please fill out the required field(s).</p>
    </form>
  </div>
</template>


<script>
import Datepicker from 'vuejs-datepicker'
import NProgress from 'nprogress'
import BaseInput from '@/components/BaseInput.vue'
import BaseSelect from '../components/BaseSelect.vue'
import BaseButton from '../components/BaseButton.vue'
import { required } from 'vuelidate/lib/validators'

export default {
  components: {
    Datepicker,
    BaseInput,
    BaseSelect,
    BaseButton
  },
  data() {
    const times = []
    for (let i = 1; i <= 24; i++) {
      times.push(i + ':00')
    }
    return {
      times,
      categories: this.$store.state.categories,
      event: this.createFreshEventObject()
    }
  },
  validations: {
    event: {
        category: { required },
        title: { required },
        description: { required },
        location: { required },
        date: { required },
        time: { required },
    }
  },
  methods: {
    createEvent() {
      this.$v.$touch()
      if (!this.$v.$invalid) {
        NProgress.start()
        this.$store
        .dispatch('event/createEvent', this.event)
        .then(() => {
          this.$router.push({
            name: 'event-show',
            params: { id: this.event.id }
          })
          this.event = this.createFreshEventObject()
        })
        .catch(() => {
          NProgress.done()
        })
      }
      
    },
    createFreshEventObject() {
      const user = this.$store.state.user.user
      const id = Math.floor(Math.random() * 10000000)

      return {
        id: id,
        user: user,
        category: '',
        organizer: user,
        title: '',
        description: '',
        location: '',
        date: '',
        time: '',
        attendees: []
      }
    }
  }
}
</script>

<style scoped>
.field {
  margin-bottom: 24px;
}
.errorMessage{
  color: red;
}
</style>
